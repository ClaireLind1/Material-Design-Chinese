              <h1>选择正确的优势</h1>
              <p>最大优势模型应该展示使用该应用的最大优势，优势数量不超过 3 个。这些优势应该要把应用定位成有价值的和人性化的，而不是仅对应用的功能做描述。</p>
              <p>在要确定使用哪些优势时，请考虑：</p>
              <ul class="lst-kix_tntcn44xacvu-0 start">
                <li>该应用能解决什么问题</li>
                <li>应用创造的最大优势是什么</li>
                <li>应用的 “牙刷特性” （意思是，你每天会使用一次或两次的功能）</li>
